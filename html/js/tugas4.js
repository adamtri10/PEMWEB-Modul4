// Contoh penggunaan tipe data angka di JavaScript

// Angka bulat (integer)
let angkaBulat = 4;
console.log(angkaBulat); // Output: 4

// Angka desimal (float)
let angkaDesimal = 2.5;
console.log(angkaDesimal); // Output: 2.5

// Penjumlahan antara angka bulat dan desimal
let total = angkaBulat + angkaDesimal;
console.log(total); // Output: 6.5

// Perkalian angka bulat dan desimal
let hasilKali = angkaBulat * angkaDesimal;
console.log(hasilKali); // Output: 10

// Pembagian menggunakan tipe data angka
let pembagian = 6 / 2;
console.log(pembagian); // Output: 3
