// Menggunakan tanda kutip ganda (double quotes)
let kalimat1 = "Hello, Adamtri!";
console.log(kalimat1); // Output: Hello, Adamtri!

// Menggunakan tanda kutip satu (single quotes)
let kalimat2 = 'JavaScript itu menyenangkan!';
console.log(kalimat2); // Output: JavaScript itu menyenangkan!

// String dengan tanda kutip ganda di dalam string yang menggunakan tanda kutip satu
let kalimat3 = 'JavaScript mengatakan, "Selamat datang!"';
console.log(kalimat3); // Output: JavaScript mengatakan, "Selamat datang!"

// String dengan tanda kutip satu di dalam string yang menggunakan tanda kutip ganda
let kalimat4 = "Dia berkata, 'Aku suka belajar coding.'";
console.log(kalimat4); // Output: Dia berkata, 'Aku suka belajar coding.'
